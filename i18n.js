  const en = Object.fromEntries(Object.entries(ko).map(([english, korean]) => [korean, english]));
  let language = localStorage.getItem("cc-language") || (navigator.language.toLowerCase().startsWith("ko") ? "ko" : "en");
  let observer;

  function translateValue(value, targetLanguage = language) {
    if (!value) return value;
    const clean = value.trim();
    if (!clean) return value;
    let translated = targetLanguage === "ko" ? ko[clean] : en[clean];
    if (!translated && targetLanguage === "ko") {
      const add = clean.match(/^Add (.+) to watchlist$/);
      const remove = clean.match(/^Remove (.+) from watchlist$/);
      if (add) translated = `${add[1]} 관심종목 추가`;
      if (remove) translated = `${remove[1]} 관심종목 제거`;
    }
    if (!translated && targetLanguage === "en") {
      const add = clean.match(/^(.+) 관심종목 추가$/);
      const remove = clean.match(/^(.+) 관심종목 제거$/);
      if (add) translated = `Add ${add[1]} to watchlist`;
      if (remove) translated = `Remove ${remove[1]} from watchlist`;
      if (/^오전\s/.test(clean)) translated = clean.replace(/^오전\s/, "AM ");
      if (/^오후\s/.test(clean)) translated = clean.replace(/^오후\s/, "PM ");
    }
    if (!translated && targetLanguage === "ko") {
      if (/^AM\s/.test(clean)) translated = clean.replace(/^AM\s/, "오전 ");
      if (/^PM\s/.test(clean)) translated = clean.replace(/^PM\s/, "오후 ");
    }
    return translated ? value.replace(clean, translated) : value;
  }

  function translateElement(root = document.body) {
    if (!root || root.closest?.("[data-i18n-ignore]")) return;
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
    const textNodes = [];
    while (walker.nextNode()) textNodes.push(walker.currentNode);
    textNodes.forEach(node => {
      if (node.parentElement?.closest("[data-i18n-ignore]")) return;
      const next = translateValue(node.nodeValue);
      if (next !== node.nodeValue) node.nodeValue = next;
    });
    const elements = root.querySelectorAll ? [root, ...root.querySelectorAll("[placeholder],[aria-label],[title]")] : [];
    elements.forEach(element => {
      ["placeholder", "aria-label", "title"].forEach(attribute => {
        if (!element.getAttribute?.(attribute) || element.closest("[data-i18n-ignore]")) return;
        const current = element.getAttribute(attribute);
        const next = translateValue(current);
        if (next !== current) element.setAttribute(attribute, next);
      });
    });
  }

  function updateSwitch() {
    document.documentElement.lang = language;
    document.querySelectorAll("[data-language]").forEach(button => {
      const active = button.dataset.language === language;
      button.classList.toggle("active", active);
      button.setAttribute("aria-pressed", String(active));
    });
  }

  function setLanguage(nextLanguage) {
    if (!["ko", "en"].includes(nextLanguage)) return;
    language = nextLanguage;
    localStorage.setItem("cc-language", language);
    updateSwitch();
    translateElement(document.body);
    window.dispatchEvent(new CustomEvent("catchingcat:language", { detail: { language } }));
  }

  function init() {
    updateSwitch();
    document.querySelectorAll("[data-language]").forEach(button => {
      button.addEventListener("click", () => setLanguage(button.dataset.language));
    });
    translateElement(document.body);
    observer = new MutationObserver(mutations => {
      mutations.forEach(mutation => {
        if (mutation.type === "characterData") {
          const next = translateValue(mutation.target.nodeValue);
          if (next !== mutation.target.nodeValue) mutation.target.nodeValue = next;
        }
        mutation.addedNodes.forEach(node => {
          if (node.nodeType === 1) translateElement(node);
          if (node.nodeType === 3) {
            const next = translateValue(node.nodeValue);
            if (next !== node.nodeValue) node.nodeValue = next;
          }
        });
      });
    });
    observer.observe(document.body, { childList: true, characterData: true, subtree: true });
  }

  window.I18N = {
    init,
    setLanguage,
    get language() { return language; },
    tr: value => translateValue(value)
  };

  init();
